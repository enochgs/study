package com.level.day02;

public class Demo03 {
    public static void main(String[] args) {
        /*
         * 测试char类型
         */
        char ch = 'A';
        System.out.println((int)ch); //输出字符的编码
        System.out.println(ch); //输出字形
        ch = '海';
        System.out.println((int)ch); //输出字符的编码
        System.out.println(ch); //输出字形
    }
}
